#include <stdio.h>
int main(){
	
	int a , b;
    a > b;
	
	printf("a == b --> %d\n" , a == b);//0
	printf("a != b --> %d\n" , a != b);//1
	printf("a > b  --> %d\n" , a > b);//1
	printf("a < b  --> %d\n" , a < b);//0
	printf("a >= b --> %d\n" , a >= b);//1
	printf("a <= b --> %d\n" , a <= b);//0
	
	
	return 0;
 }
